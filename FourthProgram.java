
public class FourthProgram {

	public static void main(String args[]) {
		//bitwise operators 
		//&, | , ^, ~
		int num1 = 12;
		byte num2 = 7;
		System.out.println(num1 & num2);
		System.out.println(num1 | num2);
		System.out.println(num1 ^ num2);
		
		//compund operators 
		
		num1 = num1 + 10;
		//num1 += 10
		
		num1 = num1 - 10; //num1 -= 10;
		
		
	}
}
